import { FruitsView } from '@/components/fruits/FruitsView';
import { getStageContent } from '@/actions/getConcepts';

// Prerender params for SSG
export function generateStaticParams() {
    return [
        { locale: 'en' },
        { locale: 'et' }
    ];
}

export const metadata = {
    title: 'Fruits (Applications) - AI Tree',
    description: 'Explore the applications and value generated by AI.'
};

export default async function FruitsPage({ params }: { params: { locale: string } }) {
    const concepts = await getStageContent('fruits', params.locale);
    return <FruitsView concepts={concepts} locale={params.locale} />;
}
